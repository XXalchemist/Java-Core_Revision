// Primitive and Reference datatypes

public class datatypes {

    public static void main (String[] args){

        // Primitive
        int num = 5;
        char alpha = 'a';
        float dec = 0.01f;
        double numd = 0.001;

        System.out.println("Integer : "+num+", Character : "+alpha+", Float decimal : "+dec+", Double decimal : "+numd);
        
        // Reference - Objects, String, Arrays etc
        String word = "Hello";

        System.out.println("This is string : "+word);
    }

}